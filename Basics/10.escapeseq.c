#include"stdio.h"

int main()
{
	// escape sequence
	
	/*
		\a - beep
		\b - backspace
		\f - formfeed
		\r - carriage return
		\v - vertical tab
		\nnn - octal number
		\xhh - hexadecimal
	----------------------------
		\n - newline
		\t - tab(horizontal)
		\\ - backslash
		\' - single quote
		\" - double quote
		\? - question mark
		\0 - null
		
	*/
	
	printf("\n\t\"I\'m Ben 10 from Cartoon Network \\ CN \"\n\n");
		
	return 0;
}
